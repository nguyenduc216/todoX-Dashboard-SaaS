-- Phase 1 repair script for video_render schema.
-- Run on database todo_saas. Safe for existing bigint-based Phase 1 tables.

CREATE SCHEMA IF NOT EXISTS video_render;

CREATE TABLE IF NOT EXISTS video_render.video_projects (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
);

ALTER TABLE video_render.video_projects
    ADD COLUMN IF NOT EXISTS tenant_id uuid NULL,
    ADD COLUMN IF NOT EXISTS user_id uuid NULL,
    ADD COLUMN IF NOT EXISTS customer_id uuid NULL,
    ADD COLUMN IF NOT EXISTS title text NULL,
    ADD COLUMN IF NOT EXISTS original_prompt text NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS total_seconds int NOT NULL DEFAULT 16,
    ADD COLUMN IF NOT EXISTS scene_seconds int NOT NULL DEFAULT 8,
    ADD COLUMN IF NOT EXISTS scene_count int NOT NULL DEFAULT 0,
    ADD COLUMN IF NOT EXISTS think_scenes boolean NOT NULL DEFAULT true,
    ADD COLUMN IF NOT EXISTS character_id bigint NULL,
    ADD COLUMN IF NOT EXISTS uploaded_character_url text NULL,
    ADD COLUMN IF NOT EXISTS storage_root text NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS public_base text NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS job_folder text NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS status text NOT NULL DEFAULT 'draft',
    ADD COLUMN IF NOT EXISTS final_video_url text NULL,
    ADD COLUMN IF NOT EXISTS final_video_path text NULL,
    ADD COLUMN IF NOT EXISTS error_message text NULL,
    ADD COLUMN IF NOT EXISTS created_at timestamptz NOT NULL DEFAULT now(),
    ADD COLUMN IF NOT EXISTS updated_at timestamptz NOT NULL DEFAULT now();

CREATE TABLE IF NOT EXISTS video_render.video_project_scenes (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
);

ALTER TABLE video_render.video_project_scenes
    ADD COLUMN IF NOT EXISTS project_id bigint NULL,
    ADD COLUMN IF NOT EXISTS tenant_id uuid NULL,
    ADD COLUMN IF NOT EXISTS scene_index int NOT NULL DEFAULT 0,
    ADD COLUMN IF NOT EXISTS title text NULL,
    ADD COLUMN IF NOT EXISTS duration_seconds int NOT NULL DEFAULT 8,
    ADD COLUMN IF NOT EXISTS scene_prompt text NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS image_prompt text NULL,
    ADD COLUMN IF NOT EXISTS video_prompt text NULL,
    ADD COLUMN IF NOT EXISTS static_image_path text NULL,
    ADD COLUMN IF NOT EXISTS static_image_url text NULL,
    ADD COLUMN IF NOT EXISTS scene_video_path text NULL,
    ADD COLUMN IF NOT EXISTS scene_video_url text NULL,
    ADD COLUMN IF NOT EXISTS status text NOT NULL DEFAULT 'draft',
    ADD COLUMN IF NOT EXISTS error_message text NULL,
    ADD COLUMN IF NOT EXISTS created_at timestamptz NOT NULL DEFAULT now(),
    ADD COLUMN IF NOT EXISTS updated_at timestamptz NOT NULL DEFAULT now();

CREATE TABLE IF NOT EXISTS video_render.video_project_events (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
);

ALTER TABLE video_render.video_project_events
    ADD COLUMN IF NOT EXISTS project_id bigint NULL,
    ADD COLUMN IF NOT EXISTS tenant_id uuid NULL,
    ADD COLUMN IF NOT EXISTS event_type text NOT NULL DEFAULT 'UNKNOWN',
    ADD COLUMN IF NOT EXISTS level text NOT NULL DEFAULT 'info',
    ADD COLUMN IF NOT EXISTS message text NOT NULL DEFAULT '',
    ADD COLUMN IF NOT EXISTS data_json jsonb NULL,
    ADD COLUMN IF NOT EXISTS created_at timestamptz NOT NULL DEFAULT now();

DO $$
BEGIN
    IF EXISTS (
        SELECT 1
          FROM information_schema.columns
         WHERE table_schema='video_render'
           AND table_name='video_projects'
           AND column_name='character_id'
           AND data_type='uuid'
    ) THEN
        ALTER TABLE video_render.video_projects DROP COLUMN character_id;
        ALTER TABLE video_render.video_projects ADD COLUMN character_id bigint NULL;
    END IF;
END $$;

CREATE INDEX IF NOT EXISTS ix_video_projects_tenant_created
    ON video_render.video_projects(tenant_id, created_at DESC);
CREATE INDEX IF NOT EXISTS ix_video_scenes_project_index
    ON video_render.video_project_scenes(project_id, scene_index);
CREATE INDEX IF NOT EXISTS ix_video_scenes_status
    ON video_render.video_project_scenes(status);
CREATE INDEX IF NOT EXISTS ix_video_events_project_created
    ON video_render.video_project_events(project_id, created_at DESC);
